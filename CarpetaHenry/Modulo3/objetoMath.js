Math.pow(2,3);
//8 (potencia)

Math.round(0.49)
//0 (redondeará el número decimal al entero más próximo)

Math.floor(5.93)
//5 (redondeará el número decimal al entero de menor valor)

Math.ceil(3.27)
//4 (redondeará un número al siguiente entero)

Math.max(1,2,3,4,5)
//5 (permitirán conocer el valor máximo de un conjunto de numeros)

Math.min(1,2,3,4,5)
//1 (permitirán conocer el valor minimo de un conjunto de numeros)

Math.random()
// permitirá generar un número aleatorio. 
//Es importante tener en cuenta que el número que se generará es decimal, y puede ser cualquiera entre el 0 y el 1

